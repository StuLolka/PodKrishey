import UIKit

extension UIColor {
    enum Global {
        static let blue = UIColor(named: "blue") ?? .blue
        static let backgroundViewController = UIColor(named: "backgroundViewController") ?? .lightGray
        static let blueGray = UIColor(named: "blueGray") ?? .blue
        static let blueButton = UIColor(named: "blueButton") ?? .blue
    }
}
